// マップの表示
void display_map(void)
{
    SDL_RenderClear(renderer);
    int i, j;
    for (j = 0; j < HEIGHT; j++) {
        for (i = 0; i < WIDTH; i++) {
            SDL_Rect dst_rect = { i * MAP_CHIP_X, j * MAP_CHIP_Y, MAP_CHIP_X, MAP_CHIP_Y }; // 転送後の領域（短形領域の左上座標）
            SDL_Rect W_rect  = {0, 390, 130, 130};
            SDL_Rect D_rect  = {0, 260, 130, 130};
            SDL_Rect A_rect  = {0, 130, 130, 130};
            SDL_Rect S_rect  = {0, 0, 130, 130};
            switch (map[j][i]) {
            case WALL:
                SDL_RenderCopy(renderer, wall_, NULL, &dst_rect);
                break;
            case OVERTIME:
                SDL_RenderCopy(renderer, overtime_, NULL, &dst_rect);
                break;
            case GOAL:
                SDL_RenderCopy(renderer, goal_, NULL, &dst_rect);
                break;
            case PLAYER:
                if (c == W)
                    SDL_RenderCopy(renderer, player_, &W_rect, &dst_rect);
                else if (c == D)
                    SDL_RenderCopy(renderer, player_, &D_rect, &dst_rect);
                else if (c == A)
                    SDL_RenderCopy(renderer, player_, &A_rect, &dst_rect);
                else if (c == S)
                    SDL_RenderCopy(renderer, player_, &S_rect, &dst_rect);
                else
                    SDL_RenderCopy(renderer, player_, &W_rect, &dst_rect);
                break;
            default:
                SDL_RenderCopy(renderer, ground_, NULL, &dst_rect);
                break;
            }
        }
    }
    SDL_Rect black_rect  = {-2500 + chara.x * 10, - 2500 + chara.y * 10, 5000, 5000};
    SDL_RenderCopy(renderer, black_, NULL, &black_rect);
    SDL_RenderPresent(renderer); // 描画データを表示
}

int move(int c)
{
    // 移動前の座標を格納
    float _x = chara.x;
    float _y = chara.y;
    // 座標の更新
    if (c == W) {
        chara.y -= SPEED;
    } else if (c == D) {
        chara.x += SPEED;
    } else if (c == A) {
        chara.x -= SPEED;
    } else if (c == S) {
        chara.y += SPEED;
    } else {
        return -1;
    }
    // プレイヤの移動先になにがあるかをチェックし
    // 各属性に応じた更新を行う
    int can_move = 1;
    int ry, rx, __y, __x = 0;
    __y = (int)_y;
    __x = (int)_x;
    ry  = (int)chara.y;
    rx  = (int)chara.x;

    switch (map[ry][rx]) {
    case WALL:
        chara.x  = _x;
        chara.y  = _y;
        can_move = -1;
        break;
    case OVERTIME:
        TimeUp += 100;
        map[__y][__x] = GROUND;
        map[ry][rx]   = PLAYER;
        tmp[ry][rx]   = GROUND;
        break;
    case GOAL:
        map[__y][__x] = tmp[__y][__x];
        map[ry][rx]   = PLAYER;
        chara.state   = 1;
        break;
    default:
        map[__y][__x] = tmp[__y][__x];
        map[ry][rx]   = PLAYER;
        break;
    }
    return can_move;
}

